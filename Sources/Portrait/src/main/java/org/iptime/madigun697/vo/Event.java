package org.iptime.madigun697.vo;

import java.util.Date;

public class Event {

	private String eventId;
	private String eventName;
	private String eventDate;
	private String userId;
	private String eventDesc;
	private String photoUrl;
	
	public Event() {
		super();
	}
	
	
	
	
	public Event(String eventId, String eventName, String eventDate,
			String userId, String eventDesc, String photoUrl) {
		super();
		this.eventId = eventId;
		this.eventName = eventName;
		this.eventDate = eventDate;
		this.userId = userId;
		this.eventDesc = eventDesc;
		this.photoUrl = photoUrl;
	}




	public Event(String eventId, String eventName, String eventDate,
			String userId, String eventDesc) {
		super();
		this.eventId = eventId;
		this.eventName = eventName;
		this.eventDate = eventDate;
		this.userId = userId;
		this.eventDesc = eventDesc;
		
	}


	public String getEventId() {
		return eventId;
	}
	public void setEventId(String eventId) {
		this.eventId = eventId;
	}
	public String getEventName() {
		return eventName;
	}
	public void setEventName(String eventName) {
		this.eventName = eventName;
	}
	public String getEventDate() {
		return eventDate;
	}
	public void setEventDate(String eventDate) {
		this.eventDate = eventDate;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}


	public String getEventDesc() {
		return eventDesc;
	}


	public void setEventDesc(String eventDesc) {
		this.eventDesc = eventDesc;
	}

	
	

	public String getPhotoUrl() {
		return photoUrl;
	}




	public void setPhotoUrl(String photoUrl) {
		this.photoUrl = photoUrl;
	}




	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("Event [eventId=");
		builder.append(eventId);
		builder.append(", eventName=");
		builder.append(eventName);
		builder.append(", eventDate=");
		builder.append(eventDate);
		builder.append(", userId=");
		builder.append(userId);
		builder.append(", eventDesc=");
		builder.append(eventDesc);
		builder.append(", photoUrl=");
		builder.append(photoUrl);
		builder.append("]");
		return builder.toString();
	}




	

	
	
}
